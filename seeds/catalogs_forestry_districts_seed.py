# ---- Генерация ----

def generate_forestry_districts(count: int) -> list[dict]:
    if count > 7000:
        raise ValueError("Слишком много записей — code_lv может не хватить")

    # ---- code_lv ----
    code_lvs = random.sample(range(3000, 10000), k=count)

    # ---- code (ограниченный пул, формат 0xx) ----
    # Имитируем распределение: ~30 кодов на сотни записей
    code_pool_size = max(10, count // 9)
    code_pool = [f"0{n:02d}" for n in random.sample(range(1, 100), k=code_pool_size)]

    rows = []
    for i in range(count):
        rows.append({
            "name": random.choice(DISTRICT_NAMES),
            "code": random.choice(code_pool),
            "code_lv": str(code_lvs[i]),
        })

    return rows